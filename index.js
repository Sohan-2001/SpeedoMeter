var imageAddr = "https://upload.wikimedia.org/wikipedia/commons/f/fe/Hungary%2C_Balatonf%C3%BCred%2C_Rabindranath_promenade_in_autumn_-_Tagore_statue.jpg"; 
var downloadSize = 2202009; //bytes

function ShowProgressMessage(msg) {
    if (console) {
        if (typeof msg == "string") {
            console.log(msg);
        } else {
            for (var i = 0; i < msg.length; i++) {
                console.log(msg[i]);
            }
        }
    }
    
    var oProgress = document.getElementById("progress");
    if (oProgress) {
        var actualHTML = (typeof msg == "string") ? msg : msg.join();
        oProgress.innerHTML = actualHTML;
    }
}
var i = 0;
var colors = ['darkred', 'red',];
document.getElementById("progress").style.color='white';
function change() {
    document.body.style.backgroundColor = colors[i];
    i++;
    if(i>=colors.length){
        i=0;  // Reset i to 0 if it exceeds colors length
    }
  }

function InitiateSpeedDetection() {
    ShowProgressMessage("CHECKING");
    setInterval(change,1000);
    window.setTimeout(MeasureConnectionSpeed, 1);
};    

if (window.addEventListener) {
    window.addEventListener('load', InitiateSpeedDetection, false);
} else if (window.attachEvent) {
    window.attachEvent('onload', InitiateSpeedDetection);
}

// 0th
function MeasureConnectionSpeed() {
    var startTime, endTime;
    var download = new Image();
    download.onload = function () {
        endTime = (new Date()).getTime();
        showResults();
    }
    
    download.onerror = function (err, msg) {
        ShowProgressMessage("ERROR");
    }
    
    startTime = (new Date()).getTime();
    var cacheBuster = "?nnn=" + startTime;
    download.src = imageAddr + cacheBuster;
    
    function showResults() {
        
        var duration = (endTime - startTime) / 1000;
        var bitsLoaded = downloadSize * 8;
        var speedBps = (bitsLoaded / duration).toFixed(2);
        var speedKbps = (speedBps / 1024).toFixed(2);
        var speedMbps = (speedKbps / 1024).toFixed(2);
        ShowProgressMessage([
            speedMbps + " MbPS"
        ]);
        MeasureConnectionSpeed1();
    }
}

